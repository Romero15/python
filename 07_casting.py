#El casting en Python se realiza mediante funciones constructoras
"""
    int () - construye un número entero de un número entero literal, un flotador literal (redondeando al número entero anterior), o una cadena literal (que proporciona la cadena representa un número entero)
    flotador () - construye un número flotante de un número entero literal, un flotador literal o una cadena literal (que proporciona la cadena representa un flotador o un número entero)
    str () - construye una cadena a partir de una amplia variedad de tipos de datos, incluyendo cuerdas, literales enteros y literales de flotador
"""
x = float(1) # x será 1.0
y = int(2.8) # y será 2
z = str(3.0)  # z será '3.0'